  if (!DICT[lang]) lang = 'sq';

  window.I18N = {
    get lang() { return lang; },
    set(l) {
      if (!DICT[l]) return;
      lang = l;
      localStorage.setItem(LS_KEY, l);
      document.documentElement.lang = l;
      window.dispatchEvent(new CustomEvent('langchange', { detail: { lang: l } }));
    },
    t(key) {
      return (DICT[lang] && DICT[lang][key]) || (DICT.sq[key]) || key;
    },
    /** Pick the right localized field from a business object (e.g. description). */
    field(obj, base) {
      if (!obj) return '';
      return obj[`${base}_${lang}`] || obj[`${base}_sq`] || obj[`${base}_en`] || '';
    },
    /** Localized name for a {name_sq,name_en} record. */
    name(obj) {
      if (!obj) return '';
      return obj[`name_${lang}`] || obj.name_sq || obj.name_en || obj.name || '';
    },
  };
  document.documentElement.lang = lang;
